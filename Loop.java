//24.using for loop,where the number stars equal to row 
package com.loop;

public class Loop {
	  
public static void main(String args[])   
	{   
	 
	int i, j, row=6;   
	 
	for(i=0; i<row; i++)   
	{   
	 
	for(j=0; j<=i; j++)   
	{   
	  
	System.out.print("* ");   
	}   
 
	System.out.println();   
	}   
}
	}

